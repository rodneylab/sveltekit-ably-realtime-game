<details>
	<summary>How to play </summary>
	<ol>
		<li>
			Wait for your turn, then click a grid line to claim it. The line will change to match your
			colour.
		</li>
		<li>To win a square, you need to claim all four of its edges.</li>
		<li>You get an extra turn if your move wins you a new square.</li>
	</ol>
</details>
